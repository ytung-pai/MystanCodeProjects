import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    carry = 0  # Carry value
    dummy = ListNode()  # Dummy node to serve as the head of the result linked list
    current = dummy  # Current node used for traversal and building the result linked list

    while l1 or l2 or carry:
        # Calculate the sum of the two nodes' values and the carry
        total = carry
        if l1:
            total += l1.val
            l1 = l1.next
        if l2:
            total += l2.val
            l2 = l2.next

        carry = total // 10  # Calculate the carry
        current.next = ListNode(total % 10)  # Create a new node for the result
        current = current.next  # Update the current node

    return dummy.next


